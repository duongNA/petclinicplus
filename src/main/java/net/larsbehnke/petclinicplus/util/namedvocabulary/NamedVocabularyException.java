package net.larsbehnke.petclinicplus.util.namedvocabulary;

public class NamedVocabularyException extends RuntimeException {

	private static final long serialVersionUID = 5116688518728390327L;

	public NamedVocabularyException() {
		super();
	}

	public NamedVocabularyException(String message, Throwable cause) {
		super(message, cause);
	}

	public NamedVocabularyException(String message) {
		super(message);
	}

	public NamedVocabularyException(Throwable cause) {
		super(cause);
	}



}
